"""
In Java: Static Typing
    DataType var = Data

In Python: Dynamic Typing
    var = Data
"""

name = None

print(name)

name = "James"
print(name)

print(type(name))

age = 25

print(type(age))

gpa = 3.5
print(type(gpa))

is_employee = True
print(type(is_employee))

s = "25"
s = int(s)
print(s * 5)


